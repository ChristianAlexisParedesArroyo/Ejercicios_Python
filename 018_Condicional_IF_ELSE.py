#En esta practica se muestra el uso del condicional if else


color = "rojo"



if color != "rojo":  #si es verdadera la condicion, entra en el if
    print("El color no es rojo.")
else:      #si no es verdadera la condicion, entonces entra en el else
    print("El color es rojo.")
