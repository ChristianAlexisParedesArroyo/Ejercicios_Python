#En esta practica se muestra el bucle while y el uso de incremento y decremento


x = 0
while x <= 15:  #Entra en el ciclo while si se cumple la condicion y para cuando deje de cumplirla
    print(x)    #se imprime el valor de x cada que entra al ciclo
    x += 5    #se incrementa el valor para que no se forme un bucle

while x >= -100:  #Entra en el ciclo while si se cumple la condicion y para cuando deje de cumplirla
    print(x)   #se imprime el valor de x cada que entra al ciclo
    x -= 20   #se decrementa el valor para que no se forme un bucle

i = 10
